package Operators;

public class Unary
{
    public static void main(String[] args)
    {
        int I=100;
        boolean b=true;
        System.out.println(I++);
        System.out.println(++I);
        System.out.println(I--);
        System.out.println(--I);
        System.out.println(!b);

    }
}
