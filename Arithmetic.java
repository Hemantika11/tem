package Operators;

public class Arithmetic
{
    public static void main(String[] args)
    {
        int A=100;
        int B=200;
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(A/B);
        System.out.println(A%B);
    }
}
