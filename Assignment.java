package Operators;

public class Assignment
{
    public static void main(String[] args)
    {
        int D=10;
        int E=20;
        System.out.println(D=E);
        System.out.println(D+=E);
        System.out.println(D-=E);
        System.out.println(D*=E);
        System.out.println(D/=E);
        System.out.println(D%=E);
        System.out.println(D^=E);

    }
}
